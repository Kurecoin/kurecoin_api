const secretKey=process.env.SECRET_KEY;

app.post('/transfer', function (request, response) {
    const amount = request.body.amount;
    const token = request.body.token;
    const address = request.body.address;
    if (token === secretKey) {
        sendToken(address, amount, function (value) {
            response.json(value);
        });
    }
    else {
        response.json({status: false, message: 'Invalid Token'});
    }

});
var KureContract = new web3.eth.Contract(abi, contractAddress, {
    from: account,
})

var sendToken = function (userWalletAddress, krcAmount, callback) {
    if ( web3.utils.isAddress(userWalletAddress) === false || krcAmount == null || krcAmount <= 0 ) {
        callback({status: false, message: 'Check data and try again'});
        return;
    }
    // KureContract.methods.balanceOf(account).call({from: account}).then(function (value) { console.log(value) });
    // return;
    try{
        krcAmount = new Number(krcAmount);
        krcAmount = krcAmount * Math.pow(10, 18);
        krcAmount = krcAmount.toString(16)
        krcAmount = web3.utils.toBN(krcAmount)

        const contractFunction = KureContract.methods.transfer(userWalletAddress, krcAmount);

        const functionAbi = contractFunction.encodeABI();

        var estimatedGas;
        var nonce;


        contractFunction.estimateGas({from: account}).then(function (gasAmount) {
            console.log(1, gasAmount)
            estimatedGas = gasAmount.toString(16);

            web3.eth.getTransactionCount(account).then(function (_nonce) {
                nonce = _nonce;

                console.log(2, nonce)

                web3.eth.getGasPrice().then(function(gasPrice){
                    console.log(3, gasPrice)
                    const txParams = {
                        gasPrice: web3.utils.toHex(gasPrice),
                        gasLimit:web3.utils.toHex(gasAmount),
                        gas:web3.utils.toHex(gasAmount),
                        to: contractAddress,
                        data: functionAbi,
                        from: account,
                        nonce: web3.utils.toHex(nonce),
                        chainId: web3.utils.toHex('1')
                    };

                    const tx = new Tx(txParams);
                    tx.sign(privateKey);

                    const serializedTx = tx.serialize();

                    web3.eth.sendSignedTransaction('0x'+serializedTx.toString('hex')).then(function(resp){
                        console.log(resp)
                    }).catch(function(err){
                        console.log(44, err);
                    });
                    callback({status: true, message: 'Transaction Sent'});

                }).catch(function(err){console.log(11,err)});
            })
        });
    }catch(error){

        //console.log(77, error)

    }
};
